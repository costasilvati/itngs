    private void jBFile1ActionPerformed(java.awt.event.ActionEvent evt) {

        File file;
        JFileChooser fc = new JFileChooser(jTFile1.getText());
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);

        int result = fc.showOpenDialog(null);

        if (result == JFileChooser.CANCEL_OPTION) {
            jTFile1.setText("");
            //boolean t = fileIsNull(file.getAbsolutePath());
        } else {
            file = fc.getSelectedFile();
            String f = file.getAbsolutePath();
            if (isFastq(file.getAbsolutePath()) && fileIsNull(file.getAbsolutePath())) {
                jTFile1.setText(file.getAbsolutePath());
                jTFile1.setEditable(false);
                if (jRBSingleEnd.isSelected()) {
                    activeQualityParameters(true);
                }
            }
        }

    }

