    /**
     * Check if file is a FASTQ
     *
     * @param file file path
     * @return true or false
     */
    private boolean isFastq(String file) {
        int end = file.length();

        boolean fastq = false;
        if (file.substring(end - 5, end).contains("fastq")) {
            fastq = true;
            return fastq;
        } else {
            JOptionPane.showMessageDialog(null,
                    "Caminho de arquivo inválido! A extensão do arquivo deve ser .fastq",
                    //"Invalid file path! The file extension should be .fastq",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
        return fastq;
    }

    /**
     * Check of file path is ok
     *
     * @param filePath path file
     * @return true to ok
     */
    public static boolean fileIsNull(String filePath) {

        boolean path = false;

        if (filePath == null || filePath.isEmpty()) {

            JOptionPane.showMessageDialog(null,
                    "Caminho inválido!",
                    //"Invalid file path!",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);

        } else {

            path = true;

        }
        return path;
    }

    /**
     * Get a string of selected button of a group
     *
     * @param buttonGroup
     * @return name of button
     */
    public String getSelectedButtonText(ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getText();
            }
        }

        return null;
    }

